    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try
        {
            this.jComboBox1.removeAllItems();
        }
        catch(Exception e)
        {
           
        }
        Database DB = DatabaseApp.ACTIVE;
        Enumeration enumElements = DB.elements();
        DataElement element;
        while (enumElements.hasMoreElements()) {
            element = (DataElement) enumElements.nextElement();
            try
            {

                String cmp = this.jTextField2.getText();
                String cmp2 = element.toString();

                if ((cmp2.contains(cmp)) || (cmp.equals("*")))
                {
                     try {
                        this.jComboBox1.addItem(element);
                    } catch (Exception e) {
                    }
                }
            } catch (Exception e) {
            }
            }
    }//GEN-LAST:event_jButton3ActionPerformed

