    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.jCheckBox1.isSelected())
        {
            try
            {
                DataElement elm = (DataElement)this.jComboBox1.getSelectedItem();
                Record rec = elm.instanciateRecord();
                float val = Float.parseFloat(this.jTextField1.getText());
                rec.setValue(val);
                rec.setLocal();
                elm.writeNewRecord(rec);
            }
            catch(Exception e)
            {
                
            }
        }
        else
        {
            try
            {
                DataElement elm = (DataElement)this.jComboBox1.getSelectedItem();
                Record rec = elm.instanciateRecord();
                float val = Float.parseFloat(this.jTextField1.getText());
                rec.setValue(val);
                rec.setSpont();
                elm.writeNewRecord(rec);
            }
            catch(Exception e)
            {

            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

